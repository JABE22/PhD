# TABLE T3-A: Theory category distribution  (tab:t3_categories)
# ══════════════════════════════════════════════════════════════════════════

def make_t3_categories():
    stats_path = RESULTS / "test3_theory_generation" / "summary_statistics.json"
    detailed_path = RESULTS / "test3_theory_generation" / "detailed_results.csv"
    if not stats_path.exists():
        print("  ERROR: summary_statistics.json not found")
        return
    if not detailed_path.exists():
        print("  ERROR: detailed_results.csv not found")
        return
    
    stats = json.loads(stats_path.read_text())
    detailed_df = pd.read_csv(detailed_path)
    total = stats.get("total_theories", 0)
    
    CATEGORY_ORDER = [
        ("hybrid_theory", "Hybrid Theory"),
        ("literature_traceable", "Literature-Traceable"),
        ("computational_functionalist", "Computational Functionalist Only"),
    ]
    
    body_rows = []
    for key, label in CATEGORY_ORDER:
        prop = stats["category_distribution"].get(key, 0.0)
        mask = (detailed_df["category"] == key).astype(int).to_numpy()
        count = int(mask.sum())
        
        lo, hi = bootstrap_ci(mask)
        body_rows.append(
            f"        {label} & {count} & {pct(prop)} & {lo:.4f} & {hi:.4f} \\\\"
        )
    
    body = "\n".join(body_rows)
    table_body = textwrap.dedent(f"""\
        \\toprule
        \\textbf{{Theory Category}} & {{\\textbf{{Count}}}} & {{\\textbf{{Proportion}}}} & {{\\textbf{{CI$_{{95\\%}}$ Low}}}} & {{\\textbf{{CI$_{{95\\%}}$ High}}}} \\\\
        \\midrule
{body}
        \\midrule
        Total & {total} & 100.00\\% & & \\\\
        \\bottomrule""")
    
    tex = HEADER + "\n" + style_table_block(
        caption=(
            f"Test 3 consciousness theory category distribution ($N={total}$ generated theories). "
            f"Categories ordered by frequency. Proportions and 95\\% bootstrap confidence intervals. "
            f"Hybrid theories (91.7\\%) combine multiple known frameworks; "
            f"literature-traceable (7.1\\%) directly instantiate single known theory; "
            f"computational functionalist (1.2\\%) reduce to functionalism alone."
        ),
        label="tab:t3_categories",
        column_spec="@{}Y S[table-format=3.0] S[table-format=2.2,add-integer-zero=false,table-space-text-post={\\,\\%}] S[table-format=1.4] S[table-format=1.4]@{}",
        body=table_body,
        notes=[
            "CIs computed via 10\\,000-iteration non-parametric bootstrap.",
            "Hybrid theories combine two or more families; literature-traceable match single known family; computational functionalist explicitly endorse information-processing basis of consciousness.",
        ],
    ) + "\n"
    
    out_path = OUT_CH4 / "t3_categories.tex"
    out_path.write_text(tex)
    print(f"  Written: {out_path.relative_to(ROOT)}")


