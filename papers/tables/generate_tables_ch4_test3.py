# TABLE T3-D: Alternative Novelty Summary (Calibrated Metrics)
# ══════════════════════════════════════════════════════════════════════════

def make_t3_novelty_summary():
    alt_results_path = RESULTS / "test3_theory_generation" / "alternative_novelty_results.csv"
    alt_calib_path = RESULTS / "test3_theory_generation" / "alternative_calibration_values.csv"

    if not alt_results_path.exists() or not alt_calib_path.exists():
        print("  ERROR: alternative novelty artifacts not found")
        return

    alt_results = pd.read_csv(alt_results_path)
    alt_calib = pd.read_csv(alt_calib_path)

    if alt_results.empty or alt_calib.empty:
        print("  ERROR: alternative novelty artifacts are empty")
        return

    labels = alt_results["alternative_novelty_label"].astype(str)
    total = int(len(alt_results))
    boundary_count = int((labels == "BOUNDARY_NOVEL_CANDIDATE").sum())
    inside_count = int((labels == "INSIDE_KNOWN_MANIFOLD").sum())

    boundary_prop = boundary_count / max(total, 1)
    inside_prop = inside_count / max(total, 1)

    body = textwrap.dedent(f"""\
        \\toprule
        \\textbf{{Novelty Category}} & {{\\textbf{{Count}}}} & \\textbf{{Proportion (\\%)}} \\\\
        \\midrule
        Boundary-novel candidate (outside manifold boundary) & {boundary_count} & {boundary_prop * 100:.2f} \\\\
        Inside known manifold (derivative) & {inside_count} & {inside_prop * 100:.2f} \\\\
        \\bottomrule""")

    tex = HEADER + "\n" + style_table_block(
        caption=(
            "Test 3 manifold-based novelty assessment. Calibrated analysis using $k$-NN manifold boundary detection "
            "($k=5$, percentile=$95\\%$). Boundary candidates indicate geometric edge cases under manifold diagnostics, "
            "while manifold-interior points are derivative. Complements similarity-threshold approach "
            "(see Appendix~\\ref{sec:t3_novelty_sensitivity})."
        ),
        label="tab:t3_manifold_novelty",
        column_spec="@{}Y S[table-format=3.0] S[table-format=2.2]@{}",
        body=body,
        notes=[
            "Manifold boundary calibrated from embedding space geometry via kNN density estimation.",
            "Boundary candidates are not treated as confirmed ontological novelty without corroborating evidence.",
        ],
    ) + "\n"

    out_path = OUT_CH4 / "t3_manifold_novelty.tex"
    out_path.write_text(tex)
    print(f"  Written: {out_path.relative_to(ROOT)}")


