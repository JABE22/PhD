def bootstrap_ci(values, n=10_000, alpha=0.05, seed=42):
    """Return (lower, upper) 95% bootstrap CI for a proportion/mean."""
    rng = np.random.default_rng(seed)
    arr = np.asarray(values, dtype=float)
    stats = [arr[rng.integers(len(arr), size=len(arr))].mean() for _ in range(n)]
    return float(np.percentile(stats, 100 * alpha / 2)), float(np.percentile(stats, 100 * (1 - alpha / 2)))


def pct(v):
    return f"{v * 100:.2f}\\%"


def fmt_ci(lo, hi, decimals=4):
    """Format a CI pair as [lo, hi] with given precision."""
    fmt = f"{{:.{decimals}f}}"
    return f"[{fmt.format(lo)}, {fmt.format(hi)}]"


def style_table_block(caption, label, column_spec, body, notes=None):
    """Render a table fragment using the chapter3 house style."""
    lines = [
        "\\begin{table}[htbp]",
        "    \\centering",
        "    \\begin{footnotesize}",
        f"    \\caption{{{caption}}}",
        f"    \\label{{{label}}}",
    ]

    if notes:
        lines.append("    \\begin{threeparttable}")

    lines.extend([
        f"    \\begin{{tabularx}}{{\\textwidth}}{{{column_spec}}}",
        body,
        "    \\end{tabularx}",
    ])

    if notes:
        lines.append("    \\begin{tablenotes}")
        lines.extend(f"        \\item {note}" for note in notes)
        lines.append("    \\end{tablenotes}")
        lines.append("    \\end{threeparttable}")

    lines.extend([
        "    \\end{footnotesize}",
        "\\end{table}",
    ])
    return "\n".join(lines)


