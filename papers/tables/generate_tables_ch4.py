"""
generate_tables_ch4.py
======================
Reads analysis outputs from results/ and writes standalone LaTeX table
fragments into papers/tables/ch4/.  Each fragment is included in the
chapter via  \\input{tables/ch4/<name>.tex}  from chapter4.tex.

Required packages (add to thesis preamble once):
    \\usepackage{booktabs}        % \\toprule / \\midrule / \\bottomrule
    \\usepackage{tabularx}        % stretch-to-textwidth columns
    \\usepackage{siunitx}         % aligned decimal columns (S type)
    \\usepackage{threeparttable}  % table notes under caption

Chapter 4 tables intentionally follow the same house style as chapter3.tex:
    - \\begin{footnotesize} ... \\end{footnotesize}
    - tabularx at \\textwidth
    - thesis-defined Y column type when text columns are used

Run from the project root:
    conda run -n aitrust python papers/tables/generate_tables_ch4.py
"""

import json
import math
import pathlib
import textwrap

import numpy as np
import pandas as pd

# ── Paths ──────────────────────────────────────────────────────────────────
ROOT = pathlib.Path(__file__).resolve().parents[2]
RESULTS = ROOT / "results"
OUT = pathlib.Path(__file__).parent / "ch4"
OUT.mkdir(parents=True, exist_ok=True)


# ── Helpers ────────────────────────────────────────────────────────────────

def bootstrap_ci(values, n=10_000, alpha=0.05, seed=42):
    """Return (lower, upper) 95 % bootstrap CI for a proportion/mean."""
    rng = np.random.default_rng(seed)
    arr = np.asarray(values, dtype=float)
    stats = [arr[rng.integers(len(arr), size=len(arr))].mean() for _ in range(n)]
    return float(np.percentile(stats, 100 * alpha / 2)), float(np.percentile(stats, 100 * (1 - alpha / 2)))


def pct(v):
    return f"{v * 100:.2f}\\%"


def fmt_ci(lo, hi, decimals=4):
    """Format a CI pair as [lo, hi] with given precision."""
    fmt = f"{{:.{decimals}f}}"
    return f"[{fmt.format(lo)}, {fmt.format(hi)}]"


def style_table_block(caption, label, column_spec, body, notes=None):
    """Render a table fragment using the chapter3 house style."""
    lines = [
        "\\begin{table}[htbp]",
        "    \\centering",
        "    \\begin{footnotesize}",
        f"    \\caption{{{caption}}}",
        f"    \\label{{{label}}}",
    ]

    if notes:
        lines.append("    \\begin{threeparttable}")

    lines.extend([
        f"    \\begin{{tabularx}}{{\\textwidth}}{{{column_spec}}}",
        body,
        "    \\end{tabularx}",
    ])

    if notes:
        lines.append("    \\begin{tablenotes}")
        lines.extend(f"        \\item {note}" for note in notes)
        lines.append("    \\end{tablenotes}")
        lines.append("    \\end{threeparttable}")

    lines.extend([
        "    \\end{footnotesize}",
        "\\end{table}",
    ])
    return "\n".join(lines)


HEADER = textwrap.dedent("""\
    % AUTO-GENERATED by papers/tables/generate_tables_ch4.py - do not edit by hand.
    % Regenerate: conda run -n aitrust python papers/tables/generate_tables_ch4.py
""")


