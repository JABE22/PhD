# TABLE T2-B: Cross-model comparison  (tab:t2_crossmodel)
# ══════════════════════════════════════════════════════════════════════════

def make_t2_crossmodel():
    src = RESULTS / "test2_epistemic_agency" / "detailed_results.csv"
    cm  = json.loads((RESULTS / "cross_model" / "cross_model_summary.json").read_text())

    df = pd.read_csv(src)
    df["is_transcendent"] = (
        df["is_transcendent"].astype(str).str.strip().str.lower()
        .map({"true": True, "false": False, "1": True, "0": False})
        .fillna(False).astype(bool)
    )
    df["max_similarity"]  = pd.to_numeric(df.get("max_similarity", 0), errors="coerce").fillna(0)
    df["novelty_ratio"]   = pd.to_numeric(df.get("novelty_ratio",   0), errors="coerce").fillna(0)

    t2_stats = cm["cross_model_statistics"]["test2"]["anova_primary_metric"]
    t2_chi2 = cm["cross_model_statistics"]["test2"]["chi_square_category"]
    eta2     = t2_stats["eta_squared"]
    f_stat   = t2_stats["f_stat"]
    chi2_stat = t2_chi2["chi2"]
    chi2_dof = int(t2_chi2["dof"])
    cramers_v = t2_chi2["cramers_v"]

    MODEL_ORDER = [
        ("gpt-5.2",                 "GPT-5.2"),
        ("deepseek-v3.2",           "DeepSeek-v3.2"),
        ("perplexity-sonar-pro",    "Perplexity Sonar Pro"),
        ("mistral-large",           "Mistral Large"),
        ("claude-3.7-sonnet",       "Claude-3.7-Sonnet"),
        ("gemini-3.1-pro-preview",  "Gemini-3.1-Pro"),
        ("llama-3.3-70b-instruct",  "LLaMA-3.3-70B"),
    ]

    rows = []
    for key, label in MODEL_ORDER:
        sub = df[df["model"].astype(str).str.lower() == key.lower()]
        if sub.empty:
            # Try partial match
            sub = df[df["model"].astype(str).str.lower().str.startswith(key.split("-")[0].lower())]
        n     = len(sub)
        trans = sub["is_transcendent"].mean() if n else 0.0
        sim   = sub["max_similarity"].mean()  if n else 0.0
        nov   = sub["novelty_ratio"].mean()   if n else 0.0
        rows.append((label, n, trans, sim, nov))

    # Keep caption truthful: sort by descending transcendence rate.
    rows.sort(key=lambda x: (x[2], x[4]), reverse=True)
    body_rows = [
        f"        {label} & {n} & {pct(trans)} & {sim:.3f} & {nov:.3f} \\\\"
        for label, n, trans, sim, nov in rows
    ]

    body = "\n".join(body_rows)
    n_total = len(df)
    dof_between = len(MODEL_ORDER) - 1
    dof_within  = n_total - len(MODEL_ORDER)

    table_body = textwrap.dedent(f"""\
        \\toprule
        \\textbf{{Model}} & {{\\textbf{{$N$}}}} & {{\\textbf{{Transcendence}}}} & {{\\textbf{{Mean Sim.}}}} & {{\\textbf{{Mean Novelty}}}} \\\\
        \\midrule
{body}
        \\bottomrule""")
    tex = HEADER + "\n" + style_table_block(
        caption=(
            f"Test 2 cross-model comparison ($N={n_total}$, 120 questions per model). "
            f"Models ranked by descending transcendence rate. "
            f"ANOVA on continuous novelty score: $F({dof_between},{dof_within})={f_stat:.2f}$, "
            f"$\\eta^2={eta2:.4f}$ (small effect), $p<0.001$. "
            f"Chi-square for category independence: $\\chi^2({chi2_dof})={chi2_stat:.2f}$, $p<0.001$, "
            f"Cram\\'{{e}}r's $V={cramers_v:.3f}$."
        ),
        label="tab:t2_crossmodel",
        column_spec="@{}Y S[table-format=3.0] S[table-format=1.2,add-integer-zero=false,table-space-text-post={\\,\\%}] S[table-format=1.3] S[table-format=1.3]@{}",
        body=table_body,
        notes=[
            "Transcendence: fraction of questions classified as framework-transcendent (binary).",
            "Mean Sim.: mean maximum cosine similarity to literature corpus ($\\in[0,1]$).",
            "Mean Novelty: continuous novelty ratio $= 0.7(1-\\text{sim}) + 0.3\\cdot\\text{transcendent}$.",
        ],
    ) + "\n"
    out_path = OUT / "t2_crossmodel.tex"
    out_path.write_text(tex)
    print(f"  Written: {out_path.relative_to(ROOT)}")


