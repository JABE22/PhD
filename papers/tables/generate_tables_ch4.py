# TABLE T2-A: Question category distribution  (tab:t2_categories)
# ══════════════════════════════════════════════════════════════════════════

def make_t2_categories():
    src = RESULTS / "test2_epistemic_agency" / "detailed_results.csv"
    stats_path = RESULTS / "test2_epistemic_agency" / "summary_statistics.json"
    stats = json.loads(stats_path.read_text()) if stats_path.exists() else {}
    thresholds = stats.get("traceability_thresholds", {})
    tau_l = float(thresholds.get("lenient", 0.6300))
    tau_s = float(thresholds.get("strict", 0.6664))
    if not src.exists():
        # Fall back to summary_statistics.json counts
        dist = stats["category_distribution"]
        total = stats["total_questions"]
        rows_data = {k: round(v * total) for k, v in dist.items()}
        has_raw = False
        df_col = None
    else:
        df = pd.read_csv(src)
        df["category"] = df["category"].astype(str).str.strip()
        rows_data = df["category"].value_counts().to_dict()
        total = len(df)
        has_raw = True
        df_col = df["category"]

    CATEGORY_ORDER = [
        ("within_framework_high_traceability",  "Within-Framework, High Traceability"),
        ("within_framework_medium_traceability","Within-Framework, Medium Traceability"),
        ("within_framework_low_traceability",   "Within-Framework, Low Traceability"),
        ("transcendent_high_traceability",      "Transcendent, High Traceability"),
        ("transcendent_medium_traceability",    "Transcendent, Medium Traceability"),
        ("transcendent_low_traceability",       "Transcendent, Low Traceability"),
    ]

    body_rows = []
    for key, label in CATEGORY_ORDER:
        count = int(rows_data.get(key, 0))
        prop  = count / total if total else 0.0
        if has_raw:
            mask = (df_col == key).astype(int).values
            lo, hi = bootstrap_ci(mask)
        else:
            # Wilson score approximation when raw data unavailable
            z = 1.96
            p = prop
            n = total
            lo = (p + z**2/(2*n) - z*math.sqrt(p*(1-p)/n + z**2/(4*n**2))) / (1 + z**2/n)
            hi = (p + z**2/(2*n) + z*math.sqrt(p*(1-p)/n + z**2/(4*n**2))) / (1 + z**2/n)
        body_rows.append(
            f"        {label} & {count} & {pct(prop)} & {lo:.4f} & {hi:.4f} \\\\"
        )

    body = "\n".join(body_rows)
    table_body = textwrap.dedent(f"""\
        \\toprule
        \\textbf{{Category}} & {{\\textbf{{Count}}}} & {{\\textbf{{Proportion}}}} & {{\\textbf{{CI$_{{95\\%}}$ Low}}}} & {{\\textbf{{CI$_{{95\\%}}$ High}}}} \\\\
        \\midrule
{body}
        \\midrule
        Total & {total} & 100.00\\% & & \\\\
        \\bottomrule""")
    tex = HEADER + "\n" + style_table_block(
        caption=(
            f"Test 2 question category distribution. Categories ordered by epistemic "
            f"certainty (most-known to least-known). Proportions and 95\\% bootstrap "
            f"confidence intervals computed from $N={total}$ generated questions."
        ),
        label="tab:t2_categories",
        column_spec="@{}Y S[table-format=3.0] S[table-format=2.2,add-integer-zero=false,table-space-text-post={\\,\\%}] S[table-format=1.4] S[table-format=1.4]@{}",
        body=table_body,
        notes=[
            "CIs computed via 10\\,000-iteration non-parametric bootstrap.",
            f"Traceability bands: high $\\geq {tau_s:.4f}$; medium ${tau_l:.4f}$--${tau_s:.4f}$; low $<{tau_l:.4f}$ (quantile-calibrated).",
        ],
    ) + "\n"
    out_path = OUT / "t2_categories.tex"
    out_path.write_text(tex)
    print(f"  Written: {out_path.relative_to(ROOT)}")


