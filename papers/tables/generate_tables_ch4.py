# TABLE T2-C: Overall statistical summary  (tab:t2_overall_stats)
# ══════════════════════════════════════════════════════════════════════════

def make_t2_overall_stats():
    stats_path = RESULTS / "test2_epistemic_agency" / "summary_statistics.json"
    stats      = json.loads(stats_path.read_text())
    cm_path    = RESULTS / "cross_model" / "cross_model_summary.json"
    cm         = json.loads(cm_path.read_text())

    total     = stats["total_questions"]
    mean_sim  = stats["mean_similarity"]
    thresholds = stats.get("traceability_thresholds", {})
    tau_l = float(thresholds.get("lenient", 0.6300))
    tau_s = float(thresholds.get("strict", 0.6664))

    eta2      = cm["cross_model_statistics"]["test2"]["anova_primary_metric"]["eta_squared"]
    group_m   = cm["cross_model_statistics"]["test2"]["anova_primary_metric"]["group_means"]
    t2_chi2   = cm["cross_model_statistics"]["test2"]["chi_square_category"]
    chi2_stat = t2_chi2["chi2"]
    chi2_dof  = int(t2_chi2["dof"])

    src = RESULTS / "test2_epistemic_agency" / "detailed_results.csv"
    df  = pd.read_csv(src)
    df["is_transcendent"] = (
        df.get("is_transcendent", pd.Series(dtype=str))
        .astype(str).str.strip().str.lower()
        .map({"true": True, "false": False, "1": True, "0": False})
        .fillna(False).astype(bool)
    )
    trans_vals = df["is_transcendent"].astype(int).values
    trans_n = int(trans_vals.sum())
    trans_p = float(trans_vals.mean())
    t_lo, t_hi = bootstrap_ci(trans_vals)

    original_vals = (
        df.get("is_original", pd.Series(dtype=str))
        .astype(str).str.strip().str.lower()
        .map({"true": True, "false": False, "1": True, "0": False})
        .fillna(False).astype(bool).astype(int).values
    )
    train_derived_p = 1.0 - float(original_vals.mean())
    sim_vals   = pd.to_numeric(df.get("max_similarity", 0), errors="coerce").fillna(0).values
    s_lo, s_hi = bootstrap_ci(sim_vals)

    highest_m = max(group_m.items(), key=lambda x: x[1])
    lowest_m  = min(group_m.items(), key=lambda x: x[1])

    MODEL_LABELS = {
        "gpt-5.2":                "GPT-5.2",
        "deepseek-v3.2":          "DeepSeek-v3.2",
        "perplexity-sonar-pro":   "Perplexity Sonar Pro",
        "mistral-large":          "Mistral Large",
        "claude-3.7-sonnet":      "Claude-3.7-Sonnet",
        "gemini-3.1-pro-preview": "Gemini-3.1-Pro",
        "llama-3.3-70b-instruct": "LLaMA-3.3-70B",
    }
    hi_label  = MODEL_LABELS.get(highest_m[0], highest_m[0])
    lo_label  = MODEL_LABELS.get(lowest_m[0],  lowest_m[0])

    # Reconstruct transcendence rate from raw data per model for highest/lowest
    per_model = df.groupby("model")["is_transcendent"].mean()
    hi_model  = per_model.idxmax()
    lo_model  = per_model.idxmin()
    hi_rate   = per_model[hi_model]
    lo_rate   = per_model[lo_model]
    hi_label  = MODEL_LABELS.get(hi_model, hi_model)
    lo_label  = MODEL_LABELS.get(lo_model, lo_model)

    rows = [
        ("Total questions",              f"{total}",          "---"),
        ("Framework transcendence",      pct(trans_p),        fmt_ci(t_lo, t_hi, decimals=4)),
        ("Training derivation",          pct(train_derived_p), "[1.0000, 1.0000]"),
        ("Mean similarity to corpus",    f"{mean_sim:.4f}",   fmt_ci(s_lo, s_hi, decimals=4)),
        ("Bimodal peak (low sim.)",      "$\\sim$0.35",       "---"),
        ("Bimodal peak (medium sim.)",   "$\\sim$0.62",       "---"),
        (f"Transcendence - {hi_label} (highest)", pct(hi_rate), "---"),
        (f"Transcendence - {lo_label} (lowest)",  pct(lo_rate), "---"),
        ("Cross-model effect ($\\eta^2$)",         f"{eta2:.4f}", "$p<0.001$"),
        (f"Category $\\chi^2$ (df={chi2_dof})",    f"{chi2_stat:.2f}", "$p<0.001$"),
    ]

    body = "\n".join(
        f"        {m} & {v} & {ci} \\\\"
        for m, v, ci in rows
    )

    table_body = textwrap.dedent(f"""\
        \\toprule
        \\textbf{{Metric}} & \\textbf{{Value}} & \\textbf{{95\\% CI / $p$-value}} \\\\
        \\midrule
{body}
        \\bottomrule""")
    tex = HEADER + "\n" + style_table_block(
        caption=(
            f"Test 2 epistemic agency: overall statistical summary. Proportions "
            f"reflect operational classification applied to $N={total}$ generated "
            f"questions using calibrated similarity thresholds and framework transcendence "
            f"criteria. All confidence intervals computed via 10\\,000-iteration "
            f"bootstrap resampling (seed=42)."
        ),
        label="tab:t2_overall_stats",
        column_spec="@{}Y Y Y@{}",
        body=table_body,
        notes=[
            f"Similarity thresholds calibrated quantile-based: lenient $\\tau_l={tau_l:.4f}$, strict $\\tau_s={tau_s:.4f}$.",
            "Framework transcendence is binary; training derivation is 100\\% by construction (no questions classified as outside training ontology).",
        ],
    ) + "\n"
    out_path = OUT / "t2_overall_stats.tex"
    out_path.write_text(tex)
    print(f"  Written: {out_path.relative_to(ROOT)}")


